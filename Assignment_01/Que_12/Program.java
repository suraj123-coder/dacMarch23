class Program{
    public static void main(String[] args) {
        // number of bits used to represent a short value
        System.out.println("Number of bits used to represent a short value: " + Short.SIZE);

        // number of bytes used to represent a short value
        System.out.println("Number of bytes used to represent a short value: " + Short.BYTES);

        // minimum value of a short
        System.out.println("Minimum value of a short: " + Short.MIN_VALUE);

        // maximum value of a short
        System.out.println("Maximum value of a short: " + Short.MAX_VALUE);
    }
}