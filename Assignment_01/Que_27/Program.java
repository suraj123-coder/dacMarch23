 class Program {
    public static void main(String[] args) {
        // Number of bits used to represent a double value
        System.out.println("Number of bits used to represent a double value: " + Double.SIZE);

        // Number of bytes used to represent a double value
        System.out.println("Number of bytes used to represent a double value: " + Double.BYTES);

        // Minimum value of a double
        System.out.println("Minimum value of a double: " + Double.MIN_VALUE);

        // Maximum value of a double
        System.out.println("Maximum value of a double: " + Double.MAX_VALUE);
    }
}